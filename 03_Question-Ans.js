//Explaine:-
/* loops are control flow structures that allow you to execute a block of code repeatedly as long as a specified 
condition is true. Loops are crucial for handling repetitive tasks, iterating over arrays, and processing data 
efficiently. 
*/

// For Loop:-
for (initialization; condition; increment/decrement) {
    // code to be repeated
}
//Example:-
for (let i = 0; i < 10; i++) {
    console.log(i);
}


// While Loop:-
while (condition) {
    // code to be repeated
}
//Example:-
let count = 0;

while (count < 10) {
    console.log(count);
    count++;
}


// Do-While Loop:-
do {
    // code to be repeated
} while (condition);
//Example:-
let x = 0;

do {
    console.log(x);
    x++;
} while (x < 5);

